int n, dp[13][1<<13];


int calc(int i, int mask){

    //trace2(i,mask);
    if (i == n)    return 1;
    int &ret = dp[i][mask];
    if (ret != -1)  return ret;
    ret = 0;

    for(int j = 0; j < n; j++)
    {
        if ((mask & (1<<j)) == 0 && j != i)
            ret += calc(i+1, mask|(1<<j));
    }
    //trace3(i,mask,ret);
    return ret;
}
